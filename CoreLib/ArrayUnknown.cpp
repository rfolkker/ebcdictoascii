#include "ArrayUnknown.h"


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CArrayUnknown::Init(int iSize, int iType)
{
	miElementSize = iSize;
	miElementType = iType;
	miNumElements = 0;
	miUsedElements = 0;
	miChunkSize = 1;
	pvArray = NULL;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CArrayUnknown::Init(int iSize, int iType, int iChunkSize)
{
	Init(iSize, iType);
	SetAllocateSize(iChunkSize);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CArrayUnknown::Allocate(int iSize, int iType, int iChunkSize)
{
	Init(iSize, iType, iChunkSize);
	GrowByChunk();
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CArrayUnknown::Allocate(int iSize, int iType, int iChunkSize, int iNumElements)
{
	miElementSize = iSize;
	miElementType = iType;
	miChunkSize = iChunkSize;
	pvArray = NULL;
	SetUsedElements(iNumElements);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CArrayUnknown::ReInit(void)
{
	Kill();
	Init(miElementSize, miElementType, miChunkSize);
}

