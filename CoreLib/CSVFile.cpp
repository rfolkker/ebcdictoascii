#include "CSVFile.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CCSVFile::Init(char cSeparator, char cDelimiter)
{
	mcFileExtended.Init(EFT_DUMMY);
	miFileSize = 0;
	mcSeparator = cSeparator;
	mcDelimiter = cDelimiter;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CCSVFile::Kill(void)
{
	mcFileExtended.Kill();
	miFileSize = 0;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CCSVFile::OpenFile(char* szName)
{
	mcFileExtended.Init(EFT_DISK);
	if (mcFileExtended.Open(szName, EFM_READ | EFM_BINARY))
	{
		miFileSize = mcFileExtended.GetFileLength();
		return TRUE;
	}
	return FALSE;
}



//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CCSVFile::Close(void)
{
	mcFileExtended.Close();
}

